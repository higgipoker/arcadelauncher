#include "pulse.h"

// --------------------------------------------------
// Pulse
// --------------------------------------------------
Pulse::Pulse() { running = false; }

// --------------------------------------------------
// Pulse
// --------------------------------------------------
Pulse::Pulse(Widget *target) : Animation(target) { start(); }

// --------------------------------------------------
// update
// --------------------------------------------------
void Pulse::update() {}

// --------------------------------------------------
// onStart
// --------------------------------------------------
void Pulse::onStart() {
    running = true;
    timer.start();
    widget->highlight(true);
}

// --------------------------------------------------
// onEnd
// --------------------------------------------------
void Pulse::onEnd() {
    widget->highlight(false);
    timer.stop();
    running = false;
}

// --------------------------------------------------
// snap
// --------------------------------------------------
void Pulse::snap() { stop(); }
